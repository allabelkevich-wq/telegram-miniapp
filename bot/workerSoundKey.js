  const vocalMatch = text.match(/\[vocal:\s*([^\]]+)\]/i);
  const vocal = vocalMatch ? vocalMatch[1].trim().slice(0, 300) : "";
  const moodMatch = text.match(/\[mood:\s*([^\]]+)\]/i);
  const mood = moodMatch ? moodMatch[1].trim().slice(0, 300) : "";
